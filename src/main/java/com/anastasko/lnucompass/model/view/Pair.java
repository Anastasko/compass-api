package com.anastasko.lnucompass.model.view;

public class Pair<U, V> {

    private U one;

    private V two;

    public U getOne() {
        return one;
    }

    public void setOne(U one) {
        this.one = one;
    }

    public V getTwo() {
        return two;
    }

    public void setTwo(V two) {
        this.two = two;
    }
}
